public class Pattern1 {
    public static void main(String[] args) {
        // funda pehla loop kitni line ke liye
        // second kitne col ke liiye
        for(int row =1;row<6;row++){
            for(int col = 1;col<6;col++){
                System.out.print("*");
            }
            System.out.println();
        }
    }
}